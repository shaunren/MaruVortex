package org.av.maruvortex;

public class BerzerkUp {
	private int x, y;
	private int radius = 12;

	public BerzerkUp(int x, int y){
		this.x = x;
		this.y = y;
	}
	public int getx(){
		return x;
	}
	public int gety(){
		return y;
	}
	public int getRadius(){
		return radius;
	}
}
